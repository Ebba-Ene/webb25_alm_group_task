const mongoose = require("mongoose");

const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      match: [/^\S+@\S+\.\S+$/, "Invalid email format"],
    },
    profileImage: {
      type: String, 
    }
  },
  { timestamps: true }
);

module.exports = mongoose.model("User", userSchema);
